package BasicPrograms;

public class Number_Divisibility {

	public static void main(String[] args) {

		int num=25;
		if(num%5==0 && num%3==0)
		{
			System.out.println("HiBye");
		}
		else if(num%3==0)
		{
			System.out.println("Hi");
		}
		else if(num%5==0)
		{
			System.out.println("Bye");
		}
	}

}
